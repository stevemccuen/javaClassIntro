package src.com.company;

public class Encaps {

    private String myName;
    private int number;

    public void setMyName(String myName){
        this.myName = myName;
    }

    public String getMyName() {
        return this.myName;
    }
    public void setNumber(int number){
        this.number = number;
    }

    public int getNumber() {
        return this.number;
    }
}
